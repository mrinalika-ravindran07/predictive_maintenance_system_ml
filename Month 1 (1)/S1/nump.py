import numpy as np

# A Python list
py_list = [1, 2, 3, 4]

# A NumPy array
np_array = np.array([1, 2, 3, 4])

print(f"Python List: {py_list}")
print(f"NumPy Array: {np_array}")

# Note: Adding lists concatenates them: [1,2] + [3,4] = [1,2,3,4]
# Adding NumPy arrays performs vector addition: [1,2] + [3,4] = [4,6]